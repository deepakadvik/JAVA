//Relational Operator
import java.util.Scanner;

public class JavaBasics2 {
    public static void main (String args []) {
        Scanner sc = new Scanner(System.in);
        int A = 10;
        int B = 5;
        System.out.println((A==B));
        System.out.println((A!=B));
        System.out.println((A>B));
        System.out.println((A<B));
        System.out.println((A>=B));
        System.out.println((A<=B));
        sc.close();
    }
}