public class Functions {
// Funtions or Methods
// Why Method? A method in Java is a block of code inside a class that performs
// a specific task. It is similar to a function, but since everything in Java
// is part of a class, we call them methods instead of functions.
    public static void printhelloworld() {
        System.out.println("Hello World!");
        System.out.println("Hello World!");
        System.out.println("Hello World!");
    }
    public static void main(String args []) {
        printhelloworld();
    }
}
