// Print the Largest among 2
import java.util.*;

public class Conditional2 {
    public static void main (String args []){
        Scanner sc = new Scanner(System.in);
        int A = 1;
        int B = 5;
        if (A > B) {
            System.out.println("A is largest among 2");
        }
        else {
            System.out.println("B is largest among 2");
        }
        sc.close();
    }
}
