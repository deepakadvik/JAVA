//Print "Deepak Kesharwani" 100 times.
import java.util.*;

public class FlowControl {
    public static void main(String args[]) {
        Scanner sc = new Scanner(System.in);
        int Count = 0;
        while (Count < 100) {
            System.out.println("Deepak Kesharwani");
            Count++;
        }
        sc.close();
    }
}
